/// Authentic Islamic duas from the Quran and Sunnah.
///
/// Only includes duas with strong basis in authentic hadith collections
/// and the Noble Quran.
class Dua {
  final String id;
  final String text;
  final String? reference;
  final String? occasion;

  const Dua({
    required this.id,
    required this.text,
    this.reference,
    this.occasion,
  });
}

