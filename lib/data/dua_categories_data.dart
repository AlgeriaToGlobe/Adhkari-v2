import 'package:flutter/material.dart';

/// A single dua with its text, reference source, and optional notes.
class DuaItem {
  final String id;
  final String text;
  final String? reference;
  final String? notes;

  const DuaItem({
    required this.id,
    required this.text,
    this.reference,
    this.notes,
  });
}

/// A category of duas (e.g., دعاء الحاجة, دعاء السفر).
class DuaCategory {
  final String id;
  final String title;
  final String subtitle;
  final IconData icon;
  final List<DuaItem> duas;

  const DuaCategory({
    required this.id,
    required this.title,
    required this.subtitle,
    required this.icon,
    required this.duas,
  });

  int get count => duas.length;
}

